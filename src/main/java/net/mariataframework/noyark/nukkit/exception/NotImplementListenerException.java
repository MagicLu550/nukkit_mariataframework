package net.mariataframework.noyark.nukkit.exception;

public class NotImplementListenerException extends ImplementException{

    public NotImplementListenerException() {
    }

    public NotImplementListenerException(String message) {
        super(message);
    }

    public NotImplementListenerException(String message, Throwable cause) {
        super(message, cause);
    }

    public NotImplementListenerException(Throwable cause) {
        super(cause);
    }

    public NotImplementListenerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
